package shared.model;

/**
 * An enumerated type which represents a keyboard key.
 */
public enum Key
{
	/**
	 * Indicates the left arrow keyboard key.
	 */
	LEFT,
	
	/**
	 * Indicates the right arrow keyboard key.
	 */
	RIGHT,
	
	/**
	 * Indicates the up arrow keyboard key. 
	 */
	UP,
	
	/**
	 * Indicates the down arrow keyboard key.
	 */
	DOWN,
	
	/**
	 * Indicates the space keyboard key.
	 */
	SPACE
}
